let day = 3;
switch(day) {
  case 1: 
    console.log("Monday"); break;
  case 2: 
    console.log("Tuesday"); break;
  case 3: 
    console.log("Wednesday"); break;

  default: 
    console.log("Invalid day");
}

let op = "add";
let a = 5, b = 3;
switch(op) {
  case "add": 
        console.log(a + b); break;
  case "subtract": 
        console.log(a - b); break;
  case "multiply": 
        console.log(a * b); break;
  case "divide": 
        console.log(a / b); break;
  default: 
        console.log("Invalid operation");
}
